package bpit.india.mentorship.poc;

public class RandomDto {

	private String userId;
	
	/**
	 * @param userId
	 */
	public RandomDto(String userId) {
		super();
		this.userId = userId;
	}

	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * @param userId the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	
	
}
